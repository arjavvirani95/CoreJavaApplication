package javaQuations;

public class Converting {

	public static void main(String[] args) {
		
		//String to integer
		String number = "10";
		int result = Integer.parseInt(number);
		System.out.println(result);
		
		//Integer to String
		int a = 100;
		String str = Integer.toString(a);
		System.out.println(str);
		
		
	}
}
